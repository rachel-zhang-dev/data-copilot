"""Agent state definition.

The state is the single source of truth that flows through every node
of the LangGraph. Each node reads from and writes to it.
"""

from __future__ import annotations

from typing import Annotated, TypedDict

from langgraph.graph.message import add_messages


class AgentState(TypedDict, total=False):
    """State carried through the agent graph.

    Fields are optional so nodes can populate them incrementally.
    """

    # Conversation history (LangGraph appends automatically via reducer).
    messages: Annotated[list, add_messages]

    # ---------- Inputs ----------
    question: str
    """The original natural-language question from the user."""

    # ---------- Intermediate ----------
    relevant_schema: str
    """Pruned database schema fed to the LLM (filled by the schema retriever)."""

    sql: str
    """SQL query generated by the LLM."""

    sql_result: list[dict]
    """Rows returned by executing the SQL."""

    error: str
    """Error message if any node fails (used by the self-healing loop)."""

    retry_count: int
    """How many times we have retried after an error."""

    # ---------- Outputs ----------
    answer: str
    """Final natural-language answer presented to the user."""
